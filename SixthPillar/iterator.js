/*
    Iterators in JavaScript are objects that provide a mechanism to traverse through a collection of data one element at a time. They follow the iterator protocol, 
    which means they have a next() method that returns an object with two properties:

        value: the next value in the iteration sequence.
        done: a boolean indicating if the end of the sequence has been reached.

    ==> Iterators are particularly useful because they can work with any data structure that implements the iterable protocol, such as Arrays, 
        Strings, Maps, Sets, and custom objects.
*/